void display_status(std::string an, std::vector<char> inc){
  std::cout << "\nIncorrect Guesses: ";

  for(int y=0; y<inc.size();y++)
  {
    std::cout << inc[y] << " ";
  }

  std::cout << "\nCodeword: ";

  for(int z=0; z<an.size();z++)
  {
    std::cout << an[z] << " ";
  }
}

